----SRC LAYER----
WITH
SRC_MDNV as ( SELECT *     from      DEV_VIEWS.DBEABP00.TEAMDNV )
//SRC_MDNV as ( SELECT *     from      TEAMDNV)
----LOGIC LAYER----
,
LOGIC_MDNV as ( SELECT 
		TRIM(FK_NDCL_CODE) AS FK_NDCL_CODE,
		TRIM(FK_DCPR_CODE) AS FK_DCPR_CODE,
		TRIM(FK_NDCP_CODE) AS FK_NDCP_CODE,
		FK_NDCV_NMBR AS FK_NDCV_NMBR,
		FK_MDDI_NMBR AS FK_MDDI_NMBR,
		cast(MDNV_CRT_DTTM  as timestamp) AS MDNV_CRT_DTTM,
		cast(EFCTV_DATE as DATE) AS EFCTV_DATE,
		cast(ENDNG_DATE as DATE) AS ENDNG_DATE,
		TRIM(CRT_USER_CODE) AS CRT_USER_CODE,
		TRIM(CRT_PRGRM_NAME) AS CRT_PRGRM_NAME,
		TRIM(DCTVT_USER_CODE) AS DCTVT_USER_CODE,
		TRIM(DCTVT_PRGRM_NAME) AS DCTVT_PRGRM_NAME,
		cast(DCTVT_DTTM as timestamp) AS DCTVT_DTTM 
				from SRC_MDNV
            )
----RENAME LAYER ----
,
RENAME_MDNV as ( SELECT FK_NDCL_CODE AS FK_NDCL_CODE,FK_DCPR_CODE AS FK_DCPR_CODE,FK_NDCP_CODE AS FK_NDCP_CODE,FK_NDCV_NMBR AS FK_NDCV_NMBR,FK_MDDI_NMBR AS FK_MDDI_NMBR,MDNV_CRT_DTTM AS MDNV_CRT_DTTM,EFCTV_DATE AS EFCTV_DATE,ENDNG_DATE AS ENDNG_DATE,CRT_USER_CODE AS CRT_USER_CODE,CRT_PRGRM_NAME AS CRT_PRGRM_NAME,DCTVT_USER_CODE AS DCTVT_USER_CODE,DCTVT_PRGRM_NAME AS DCTVT_PRGRM_NAME,DCTVT_DTTM AS DCTVT_DTTM 
			from      LOGIC_MDNV
        )
----FILTER LAYER(uses aliases)----
,

        FILTER_MDNV as ( SELECT  * 
			from     RENAME_MDNV 
            
        )
----JOIN LAYER----
,
 JOIN_MDNV as ( SELECT * 
			from  FILTER_MDNV )
 SELECT * FROM JOIN_MDNV