
    
    



select count(*) as validation_errors
from (

    select
        INVOICE_NUMBER||EDIT_CODE||EOB_CODE||HDR_EDIT_SOURCE||HDR_EOB_SOURCE||HDR_EDIT_PHASE_APPLIED||HDR_EOB_PHASE_APPLIED||EOB_ENTRY_DATE||EDIT_ENTRY_DATE

    from STAGING.DST_MEDICAL_INVOICE_HEADER_EDIT_EOB
    where INVOICE_NUMBER||EDIT_CODE||EOB_CODE||HDR_EDIT_SOURCE||HDR_EOB_SOURCE||HDR_EDIT_PHASE_APPLIED||HDR_EOB_PHASE_APPLIED||EOB_ENTRY_DATE||EDIT_ENTRY_DATE is not null
    group by INVOICE_NUMBER||EDIT_CODE||EOB_CODE||HDR_EDIT_SOURCE||HDR_EOB_SOURCE||HDR_EDIT_PHASE_APPLIED||HDR_EOB_PHASE_APPLIED||EOB_ENTRY_DATE||EDIT_ENTRY_DATE
    having count(*) > 1

) validation_errors


