----SRC LAYER----
WITH
SRC_DCVD as ( SELECT *     from      DEV_VIEWS.DBEABP00.TEADCVD )
----LOGIC LAYER----
,
LOGIC_DCVD as ( SELECT 
		TRIM(FK_NDCL_CODE) AS FK_NDCL_CODE,
		TRIM(FK_DCPR_CODE) AS FK_DCPR_CODE,
		TRIM(FK_NDCP_CODE) AS FK_NDCP_CODE,
		FK_NDCV_NMBR AS FK_NDCV_NMBR,
		DCVD_CRT_DTTM AS DCVD_CRT_DTTM,
		cast(BGNG_DATE as DATE) AS BGNG_DATE,
		cast(ENDNG_DATE as DATE) AS ENDNG_DATE,
		TRIM(CRT_USER_CODE) AS CRT_USER_CODE,
		TRIM(CRT_PRGRM_NAME) AS CRT_PRGRM_NAME,
		TRIM(DCTVT_USER_CODE) AS DCTVT_USER_CODE,
		TRIM(DCTVT_PRGRM_NAME) AS DCTVT_PRGRM_NAME,
		DCTVT_DTTM AS DCTVT_DTTM 
				from SRC_DCVD
            )
----RENAME LAYER ----
,
RENAME_DCVD as ( SELECT FK_NDCL_CODE AS FK_NDCL_CODE,FK_DCPR_CODE AS FK_DCPR_CODE,FK_NDCP_CODE AS FK_NDCP_CODE,FK_NDCV_NMBR AS FK_NDCV_NMBR,DCVD_CRT_DTTM AS DCVD_CRT_DTTM,BGNG_DATE AS BGNG_DATE,ENDNG_DATE AS ENDNG_DATE,CRT_USER_CODE AS CRT_USER_CODE,CRT_PRGRM_NAME AS CRT_PRGRM_NAME,DCTVT_USER_CODE AS DCTVT_USER_CODE,DCTVT_PRGRM_NAME AS DCTVT_PRGRM_NAME,DCTVT_DTTM AS DCTVT_DTTM 
			from      LOGIC_DCVD
        )
----FILTER LAYER(uses aliases)----
,

        FILTER_DCVD as ( SELECT  * 
			from     RENAME_DCVD 
            
        )
----JOIN LAYER----
,
 JOIN_DCVD as ( SELECT * 
			from  FILTER_DCVD )
 SELECT * FROM JOIN_DCVD