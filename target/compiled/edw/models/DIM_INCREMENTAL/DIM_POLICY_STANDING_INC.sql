    POLICY_ACTIVE_IND, 
    POLICY_STATUS_CODE, 
    POLICY_STATUS_DESC, 
    POLICY_TYPE_CODE, 
    POLICY_TYPE_DESC, 
    PRIMARY_SOURCE_SYSTEM, 
    STATUS_REASON_CODE, 
    STATUS_REASON_DESC, 
    UNIQUE_ID_KEY
    from DEV_EDW_32600145.DIM_INCREMENTAL.DIM_POLICY_STANDING_INC
