WITH diff as (
select
    PBM_PRICING_SOURCE_CODE, 
    PBM_PRICING_SOURCE_DESC, 
    PRICE_TYPE_CODE, 
    PRICE_TYPE_DESC, 
    PRIMARY_SOURCE_SYSTEM, 
    UNIQUE_ID_KEY
    from  EDW_STAGING_DIM.DIM_PBM_PRICING_METHOD 
minus
select 
    PBM_PRICING_SOURCE_CODE, 
    PBM_PRICING_SOURCE_DESC, 
    PRICE_TYPE_CODE, 
    PRICE_TYPE_DESC, 
    PRIMARY_SOURCE_SYSTEM, 
    UNIQUE_ID_KEY
    from DEV_EDW_32600145.DIM_INCREMENTAL.DIM_PBM_PRICING_METHOD_INC 
),
src as ( select * from EDW_STAGING_DIM.DIM_PBM_PRICING_METHOD )
select src.* from src
inner join diff on ( diff.UNIQUE_ID_KEY = src.UNIQUE_ID_KEY )