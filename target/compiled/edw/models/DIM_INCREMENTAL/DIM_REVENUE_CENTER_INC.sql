WITH diff as (
select
    CURRENT_RECORD_IND, 
    HOSPITAL_REVENUE_CENTER_CODE, 
    HOSPITAL_REVENUE_CENTER_DESC, 
    PRIMARY_SOURCE_SYSTEM, 
    REVENUE_CENTER_EFFECTIVE_DATE, 
    REVENUE_CENTER_EXPIRATION_DATE, 
    REVENUE_CENTER_PAYMENT_CATEGORY, 
    UNIQUE_ID_KEY
    from  EDW_STAGING_DIM.DIM_REVENUE_CENTER 
where current_record_ind = 'Y'
minus
select 
    CURRENT_RECORD_IND, 
    HOSPITAL_REVENUE_CENTER_CODE, 
    HOSPITAL_REVENUE_CENTER_DESC, 
    PRIMARY_SOURCE_SYSTEM, 
    REVENUE_CENTER_EFFECTIVE_DATE, 
    REVENUE_CENTER_EXPIRATION_DATE, 
    REVENUE_CENTER_PAYMENT_CATEGORY, 
    UNIQUE_ID_KEY
    from DEV_EDW_32600145.DIM_INCREMENTAL.DIM_REVENUE_CENTER_INC 
where current_record_ind = 'Y'
),
src as ( select * from EDW_STAGING_DIM.DIM_REVENUE_CENTER )
select src.* from src
inner join diff on ( diff.UNIQUE_ID_KEY = src.UNIQUE_ID_KEY )