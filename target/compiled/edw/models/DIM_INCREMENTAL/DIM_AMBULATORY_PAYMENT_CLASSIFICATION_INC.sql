WITH diff as (
select
    APC_AMOUNT, 
    APC_CODE, 
    APC_CODE_STATUS_CODE, 
    APC_CODE_STATUS_DESC, 
    APC_DESC, 
    APC_EFFECTIVE_DATE, 
    APC_EXPIRATION_DATE, 
    APC_RELATIVE_WEIGHT_RATE, 
    CURRENT_RECORD_IND, 
    PRIMARY_SOURCE_SYSTEM, 
    UNIQUE_ID_KEY
    from  EDW_STAGING_DIM.DIM_AMBULATORY_PAYMENT_CLASSIFICATION 
where current_record_ind = 'Y'
minus
select 
    APC_AMOUNT, 
    APC_CODE, 
    APC_CODE_STATUS_CODE, 
    APC_CODE_STATUS_DESC, 
    APC_DESC, 
    APC_EFFECTIVE_DATE, 
    APC_EXPIRATION_DATE, 
    APC_RELATIVE_WEIGHT_RATE, 
    CURRENT_RECORD_IND, 
    PRIMARY_SOURCE_SYSTEM, 
    UNIQUE_ID_KEY
    from DEV_EDW_32600145.DIM_INCREMENTAL.DIM_AMBULATORY_PAYMENT_CLASSIFICATION_INC 
where current_record_ind = 'Y'
),
src as ( select * from EDW_STAGING_DIM.DIM_AMBULATORY_PAYMENT_CLASSIFICATION )
select src.* from src
inner join diff on ( diff.UNIQUE_ID_KEY = src.UNIQUE_ID_KEY )