WITH diff as (
select
    CURRENT_RECORD_IND, 
    LINE_PLACE_OF_SERVICE_CODE, 
    LINE_PLACE_OF_SERVICE_DESC, 
    OUT_OF_OFFICE_IND, 
    PLACE_OF_SERVICE_EFFECTIVE_DATE, 
    PLACE_OF_SERVICE_END_DATE, 
    PRIMARY_SOURCE_SYSTEM, 
    UNIQUE_ID_KEY
    from  EDW_STAGING_DIM.DIM_PLACE_OF_SERVICE 
where current_record_ind = 'Y'
minus
select 
    CURRENT_RECORD_IND, 
    LINE_PLACE_OF_SERVICE_CODE, 
    LINE_PLACE_OF_SERVICE_DESC, 
    OUT_OF_OFFICE_IND, 
    PLACE_OF_SERVICE_EFFECTIVE_DATE, 
    PLACE_OF_SERVICE_END_DATE, 
    PRIMARY_SOURCE_SYSTEM, 
    UNIQUE_ID_KEY
    from DEV_EDW_32600145.DIM_INCREMENTAL.DIM_PLACE_OF_SERVICE_INC 
where current_record_ind = 'Y'
),
src as ( select * from EDW_STAGING_DIM.DIM_PLACE_OF_SERVICE )
select src.* from src
inner join diff on ( diff.UNIQUE_ID_KEY = src.UNIQUE_ID_KEY )