WITH diff as (
select
    PRICE_TYPE_CODE, 
    PRICE_TYPE_DESC, 
    PRIMARY_SOURCE_SYSTEM, 
    UNIQUE_ID_KEY
    from  EDW_STAGING_DIM.DIM_DRUG_PRICING_TYPE 
minus
select 
    PRICE_TYPE_CODE, 
    PRICE_TYPE_DESC, 
    PRIMARY_SOURCE_SYSTEM, 
    UNIQUE_ID_KEY
    from DEV_EDW_32600145.DIM_INCREMENTAL.DIM_DRUG_PRICING_TYPE_INC 
),
src as ( select * from EDW_STAGING_DIM.DIM_DRUG_PRICING_TYPE )
select src.* from src
inner join diff on ( diff.UNIQUE_ID_KEY = src.UNIQUE_ID_KEY )