SCD1 as ( SELECT EDIT_CODE, APPLIED_BY_DESCRIPTION, UNIQUE_ID_KEY    
	from      STAGING.DSV_EDIT ),
SCD2 as ( SELECT *    
	from      EDW_STAGING_SNAPSHOT.DIM_EDIT_SNAPSHOT_STEP1 ),
