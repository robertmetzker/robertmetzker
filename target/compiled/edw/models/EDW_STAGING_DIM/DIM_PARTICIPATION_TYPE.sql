

 WITH  SCD AS ( 
	SELECT  UNIQUE_ID_KEY,
     last_value(PARTICIPATION_TYPE_CODE) over 
        (partition by UNIQUE_ID_KEY 
        order by UNIQUE_ID_KEY 
            ROWS BETWEEN UNBOUNDED PRECEDING AND UNBOUNDED FOLLOWING
            ) as PARTICIPATION_TYPE_CODE, 
     last_value(PARTICIPATION_PRIMARY_IND) over 
        (partition by UNIQUE_ID_KEY 
        order by UNIQUE_ID_KEY 
            ROWS BETWEEN UNBOUNDED PRECEDING AND UNBOUNDED FOLLOWING
            ) as PARTICIPATION_PRIMARY_IND, 
     last_value(PARTICIPATION_TYPE_DESC) over 
        (partition by UNIQUE_ID_KEY 
        order by UNIQUE_ID_KEY 
            ROWS BETWEEN UNBOUNDED PRECEDING AND UNBOUNDED FOLLOWING
            ) as PARTICIPATION_TYPE_DESC
     
	FROM EDW_STAGING.DIM_PARTICIPATION_TYPE_SCDALL_STEP2)

----ETL LAYER----
,
ETL as ( SELECT 
              UNIQUE_ID_KEY AS PARTICIPATION_TYPE_HKEY
            , UNIQUE_ID_KEY
            , PARTICIPATION_TYPE_CODE
            , PARTICIPATION_PRIMARY_IND
            , PARTICIPATION_TYPE_DESC
	        , CURRENT_TIMESTAMP AS  LOAD_DATETIME
            , TRY_TO_TIMESTAMP('Invalid') AS UPDATE_DATETIME
            , 'CORESUITE' AS PRIMARY_SOURCE_SYSTEM 
    from SCD
)

SELECT * FROM ETL