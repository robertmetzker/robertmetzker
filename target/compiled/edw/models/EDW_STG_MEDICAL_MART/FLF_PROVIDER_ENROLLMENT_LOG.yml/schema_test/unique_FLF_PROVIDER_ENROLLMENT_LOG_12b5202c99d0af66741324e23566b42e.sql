
    
    



select count(*) as validation_errors
from (

    select
        PROVIDER_HKEY||ENROLLMENT_STATUS_HKEY||DERIVED_EFFECTIVE_DATE_KEY||DERIVED_ENDING_DATE_KEY

    from EDW_STG_MEDICAL_MART.FLF_PROVIDER_ENROLLMENT_LOG
    where PROVIDER_HKEY||ENROLLMENT_STATUS_HKEY||DERIVED_EFFECTIVE_DATE_KEY||DERIVED_ENDING_DATE_KEY is not null
    group by PROVIDER_HKEY||ENROLLMENT_STATUS_HKEY||DERIVED_EFFECTIVE_DATE_KEY||DERIVED_ENDING_DATE_KEY
    having count(*) > 1

) validation_errors


