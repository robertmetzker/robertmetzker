

      create or replace  table DEV_EDW.EDW_STAGING_DIM.DIM_POLICY_FINANCIAL_TRANSACTION_COMMENT  as
      (

 WITH  SCD AS ( 
	SELECT  UNIQUE_ID_KEY,
         last_value(FINANCIAL_TRANSACTION_COMMENT_TEXT) over 
        (partition by UNIQUE_ID_KEY 
        order by UNIQUE_ID_KEY 
            ROWS BETWEEN UNBOUNDED PRECEDING AND UNBOUNDED FOLLOWING
            ) as FINANCIAL_TRANSACTION_COMMENT_TEXT
	FROM EDW_STAGING.DIM_POLICY_FINANCIAL_TRANSACTION_COMMENT_SCDALL_STEP2),

ETL AS( 
SELECT
     md5(cast(
    
    coalesce(cast(FINANCIAL_TRANSACTION_COMMENT_TEXT as 
    varchar
), '')

 as 
    varchar
)) As FINANCIAL_TRANSACTION_COMMENT_HKEY
    ,UNIQUE_ID_KEY
    ,FINANCIAL_TRANSACTION_COMMENT_TEXT
    ,CURRENT_TIMESTAMP AS  LOAD_DATETIME
    ,TRY_TO_TIMESTAMP('Invalid') AS UPDATE_DATETIME
    ,'CORESUITE' AS PRIMARY_SOURCE_SYSTEM 
 from SCD
)

select * from ETL
      );
    