

      create or replace  table DEV_EDW.EDW_STAGING_DIM.DIM_ICD_ADMISSION_PRESENCE  as
      (

 WITH  SCD AS ( 
	SELECT  UNIQUE_ID_KEY,
     last_value(PRESENT_ON_ADMISSION_CODE) over 
        (partition by UNIQUE_ID_KEY 
        order by UNIQUE_ID_KEY 
            ROWS BETWEEN UNBOUNDED PRECEDING AND UNBOUNDED FOLLOWING
            ) as PRESENT_ON_ADMISSION_CODE, 
     last_value(PRESENT_ON_ADMISSION_DESC) over 
        (partition by UNIQUE_ID_KEY 
        order by UNIQUE_ID_KEY 
            ROWS BETWEEN UNBOUNDED PRECEDING AND UNBOUNDED FOLLOWING
            ) as PRESENT_ON_ADMISSION_DESC
     
	FROM  EDW_STAGING.DIM_ICD_ADMISSION_PRESENCE_SCDALL_STEP2),

ETL AS (
select UNIQUE_ID_KEY AS PRESENT_ON_ADMISSION_HKEY 
    , * 
    , CURRENT_TIMESTAMP AS  LOAD_DATETIME
    , TRY_TO_TIMESTAMP('Invalid') AS UPDATE_DATETIME
    , 'CAM' AS PRIMARY_SOURCE_SYSTEM 
    from SCD
)

SELECT * FROM ETL
      );
    