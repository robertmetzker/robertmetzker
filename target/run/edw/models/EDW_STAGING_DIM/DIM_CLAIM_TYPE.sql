

      create or replace  table DEV_EDW.EDW_STAGING_DIM.DIM_CLAIM_TYPE  as
      (

 WITH  SCD AS ( 
	SELECT  UNIQUE_ID_KEY,
     last_value(CLAIM_TYPE_CODE) over 
        (partition by UNIQUE_ID_KEY 
        order by UNIQUE_ID_KEY 
            ROWS BETWEEN UNBOUNDED PRECEDING AND UNBOUNDED FOLLOWING
            ) as CLAIM_TYPE_CODE, 
     last_value(CLAIM_TYPE_DESC) over 
        (partition by UNIQUE_ID_KEY 
        order by UNIQUE_ID_KEY 
            ROWS BETWEEN UNBOUNDED PRECEDING AND UNBOUNDED FOLLOWING
            ) as CLAIM_TYPE_DESC
     
	FROM EDW_STAGING.DIM_CLAIM_TYPE_SCDALL_STEP2),
ETL AS( 
SELECT
     UNIQUE_ID_KEY AS CLAIM_TYPE_HKEY
    ,UNIQUE_ID_KEY
    ,CLAIM_TYPE_CODE
    ,CLAIM_TYPE_DESC
    ,CURRENT_TIMESTAMP AS  LOAD_DATETIME
    ,TRY_TO_TIMESTAMP('Invalid') AS UPDATE_DATETIME
    ,'CORESUITE' AS PRIMARY_SOURCE_SYSTEM 
 from SCD
)
select * from ETL
      );
    