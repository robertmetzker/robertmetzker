

      create or replace  table DEV_EDW.STAGING.STG_NDC_PRIOR_AUTH_IND  as
      (----SRC LAYER----
WITH
SRC_ONPA as ( SELECT *     from      DEV_VIEWS.DBEABP00.TEAONPA ),
----LOGIC LAYER----
LOGIC_ONPA as ( SELECT 
		TRIM(FK_NDCL_CODE) AS FK_NDCL_CODE,
		TRIM(FK_DCPR_CODE) AS FK_DCPR_CODE,
		TRIM(FK_NDCP_CODE) AS FK_NDCP_CODE,
		FK_NDCV_NMBR AS FK_NDCV_NMBR,
		ONPA_CRT_DTTM AS ONPA_CRT_DTTM,
		TRIM(IND) AS IND,
		cast(EFCTV_DATE as DATE) AS EFCTV_DATE,
		cast(ENDNG_DATE as DATE) AS ENDNG_DATE,
		TRIM(CRT_USER_CODE) AS CRT_USER_CODE,
		TRIM(CRT_PRGRM_NAME) AS CRT_PRGRM_NAME,
		TRIM(DCTVT_USER_CODE) AS DCTVT_USER_CODE,
		TRIM(DCTVT_PRGRM_NAME) AS DCTVT_PRGRM_NAME,
		DCTVT_DTTM AS DCTVT_DTTM 
				from SRC_ONPA
            )
----RENAME LAYER ----
,
RENAME_ONPA as ( SELECT FK_NDCL_CODE AS FK_NDCL_CODE,FK_DCPR_CODE AS FK_DCPR_CODE,FK_NDCP_CODE AS FK_NDCP_CODE,FK_NDCV_NMBR AS FK_NDCV_NMBR,ONPA_CRT_DTTM AS ONPA_CRT_DTTM,IND AS IND,EFCTV_DATE AS EFCTV_DATE,ENDNG_DATE AS ENDNG_DATE,CRT_USER_CODE AS CRT_USER_CODE,CRT_PRGRM_NAME AS CRT_PRGRM_NAME,DCTVT_USER_CODE AS DCTVT_USER_CODE,DCTVT_PRGRM_NAME AS DCTVT_PRGRM_NAME,DCTVT_DTTM AS DCTVT_DTTM 
			from      LOGIC_ONPA
        )
----FILTER LAYER(uses aliases)----
,

        FILTER_ONPA as ( SELECT  * 
			from     RENAME_ONPA 
            
        )
----JOIN LAYER----
,
 JOIN_ONPA as ( SELECT * 
			from  FILTER_ONPA )
 SELECT * FROM JOIN_ONPA
      );
    