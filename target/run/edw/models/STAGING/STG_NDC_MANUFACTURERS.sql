

      create or replace  table DEV_EDW.STAGING.STG_NDC_MANUFACTURERS  as
      (----SRC LAYER----
WITH
SRC_MINV as ( SELECT *     from      DEV_VIEWS.DBEABP00.TEAMINV ),
----LOGIC LAYER----
LOGIC_MINV as ( SELECT 
		TRIM(FK_NDCL_CODE) AS FK_NDCL_CODE,
		TRIM(FK_DCPR_CODE) AS FK_DCPR_CODE,
		TRIM(FK_NDCP_CODE) AS FK_NDCP_CODE,
		FK_NDCV_NMBR AS FK_NDCV_NMBR,
		FK_MLBI_NMBR AS FK_MLBI_NMBR,
		MINV_CRT_DTTM AS MINV_CRT_DTTM,
		cast(EFCTV_DATE as DATE) AS EFCTV_DATE,
		cast(ENDNG_DATE as DATE) AS ENDNG_DATE,
		TRIM(CRT_USER_CODE) AS CRT_USER_CODE,
		TRIM(CRT_PRGRM_NAME) AS CRT_PRGRM_NAME,
		TRIM(DCTVT_USER_CODE) AS DCTVT_USER_CODE,
		TRIM(DCTVT_PRGRM_NAME) AS DCTVT_PRGRM_NAME,
		DCTVT_DTTM AS DCTVT_DTTM 
				from SRC_MINV
            )
----RENAME LAYER ----
,
RENAME_MINV as ( SELECT FK_NDCL_CODE AS FK_NDCL_CODE,FK_DCPR_CODE AS FK_DCPR_CODE,FK_NDCP_CODE AS FK_NDCP_CODE,FK_NDCV_NMBR AS FK_NDCV_NMBR,FK_MLBI_NMBR AS FK_MLBI_NMBR,MINV_CRT_DTTM AS MINV_CRT_DTTM,EFCTV_DATE AS EFCTV_DATE,ENDNG_DATE AS ENDNG_DATE,CRT_USER_CODE AS CRT_USER_CODE,CRT_PRGRM_NAME AS CRT_PRGRM_NAME,DCTVT_USER_CODE AS DCTVT_USER_CODE,DCTVT_PRGRM_NAME AS DCTVT_PRGRM_NAME,DCTVT_DTTM AS DCTVT_DTTM 
			from      LOGIC_MINV
        )
----FILTER LAYER(uses aliases)----
,

        FILTER_MINV as ( SELECT  * 
			from     RENAME_MINV 
            
        )
----JOIN LAYER----
,
 JOIN_MINV as ( SELECT * 
			from  FILTER_MINV )
 SELECT * FROM JOIN_MINV
      );
    