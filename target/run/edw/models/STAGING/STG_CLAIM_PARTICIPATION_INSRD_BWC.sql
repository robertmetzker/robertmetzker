

      create or replace  table DEV_EDW.STAGING.STG_CLAIM_PARTICIPATION_INSRD_BWC  as
      (---- SRC LAYER ----
WITH
CPI as ( SELECT *     from     DEV_VIEWS.PCMP.CLAIM_PARTICIPATION_INSRD ),
BCPI as ( SELECT *     from     DEV_VIEWS.PCMP.BWC_CLAIM_PARTICIPATION_INSRD )
-- FINAL LAYER --
(SELECT CPI.CLM_PTCP_INSRD_ID,
        CPI.CLM_PTCP_ID,
        CPI.CLM_PTCP_INSRD_SELF_INSRD_IND,
        CPI.CLM_PTCP_INSRD_CNTS_IND,
        CPI.AUDIT_USER_ID_CREA,
        CPI.AUDIT_USER_CREA_DTM,
        CPI.AUDIT_USER_ID_UPDT,
        CPI.AUDIT_USER_UPDT_DTM,
        CPI.VOID_IND,
        BCPI.CLM_PTCP_INSRD_PEO_IND,
        BCPI.DW_CHANGE_DT
 FROM CPI
 LEFT  JOIN BCPI
 ON (CPI.CLM_PTCP_INSRD_ID = BCPI.CLM_PTCP_INSRD_ID) )
      );
    